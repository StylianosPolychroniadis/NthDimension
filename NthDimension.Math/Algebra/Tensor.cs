    /// <summary>
    /// The Tensor Library contains Class Tensor which provides many 
    /// static methods for making various Tensor operations on objects
    /// derived from the class or on arrays defined as double. The 
    /// '+','-','*' operators are overloaded to work with the objects
    /// derived from the Tensor class.
    /// </summary>
    public class Tensor
    {

        /// <summary>
        /// Returns a hash code for this instance.
        /// </summary>
        /// <returns>
        /// A hash code for this instance, suitable for use in hashing algorithms and data structures like a hash table. 
        /// </returns>
        public override int GetHashCode()
        {
            return in_Tensor.GetHashCode();
        }

        /// <summary>
        /// The Tensor.
        /// </summary>
        private double[,] in_Tensor;

        /// <summary>
        /// Tensor object constructor, constructs an empty
        /// Tensor with dimensions: rows = noRows and cols = noCols.
        /// </summary>
        /// <param name="noRows"> no. of rows in this Tensor </param>
        /// <param name="noCols"> no. of columns in this Tensor</param>
        public Tensor(int noRows, int noCols)
        {
            this.in_Tensor = new double[noRows, noCols];
        }

        /// <summary>
        /// Tensor object constructor, constructs a Tensor from an
        /// already defined array object.
        /// </summary>
        /// <param name="Mat">the array the Tensor will contain</param>
        public Tensor(double[,] Mat)
        {
            this.in_Tensor = (double[,])Mat.Clone();
        }

        public Tensor(Tensor tensor)
        {
            this.in_Tensor = (double[,])tensor.in_Tensor.Clone();
        }

        /// <summary>
        /// Set or get an element from the Tensor
        /// </summary>
        public double this[int Row, int Col]
        {
            get { return this.in_Tensor[Row, Col]; }
            set { this.in_Tensor[Row, Col] = value; }
        }

        /// <summary>
        /// Creates a Tensor from a column major array.
        /// </summary>
        /// <param name="columnMajorArray">The column major array.</param>
        /// <param name="rows">The rows.</param>
        /// <param name="columns">The columns.</param>
        /// <returns>The Tensor.</returns>
        public static Tensor FromColumnMajorArray(double[] columnMajorArray, int rows, int columns)
        {
            //  Create the Tensor.
            Tensor tensor = new Tensor(rows, columns);

            //  Set the values.
            int index = 0;
            for (int i = 0; i < rows; i++)
                for (int j = 0; j < columns; j++)
                    tensor[i, j] = columnMajorArray[index++];

            //  Return the Tensor.
            return tensor;
        }

        /// <summary>
        /// Creates a Tensor from a row major array.
        /// </summary>
        /// <param name="columnMajorArray">The column major array.</param>
        /// <param name="rows">The rows.</param>
        /// <param name="columns">The columns.</param>
        /// <returns>The Tensor.</returns>
        public static Tensor FromRowMajorArray(double[] rowMajorArray, int rows, int columns)
        {
            //  Create the Tensor.
            Tensor tensor = new Tensor(rows, columns);

            //  Set the values.
            int index = 0;
            for (int i = 0; i < columns; i++)
                for (int j = 0; j < rows; j++)
                    tensor[j, i] = rowMajorArray[index++];

            //  Return the Tensor.
            return tensor;
        }

        /// <summary>
        /// Creates a Tensor from a segment of another Tensor.
        /// </summary>
        /// <param name="rhs">The RHS.</param>
        /// <param name="rows">The rows.</param>
        /// <param name="cols">The cols.</param>
        public void FromOtherTensor(Tensor rhs, int rows, int cols)
        {
            //  Check dimensions.
            if (rows > Rows || rows > rhs.Rows || cols > Columns || cols > rhs.Columns)
                throw new TensorDimensionException();

            for (int i = 0; i < rows; i++)
            {
                for (int j = 0; j < cols; j++)
                {
                    this[i, j] = rhs[i, j];
                }
            }
        }

        #region "Public Properties"
        /// <summary>
        /// Set or get the no. of rows in the Tensor.
        /// Warning: Setting this property will delete all of
        /// the elements of the Tensor and set them to zero.
        /// </summary>
        public int Rows
        {
            get { return this.in_Tensor.GetUpperBound(0) + 1; }
            set { this.in_Tensor = new double[value, this.in_Tensor.GetUpperBound(0)]; }
        }

        /// <summary>
        /// Set or get the no. of columns in the Tensor.
        /// Warning: Setting this property will delete all of
        /// the elements of the Tensor and set them to zero.
        /// </summary>
        public int Columns
        {
            get { return this.in_Tensor.GetUpperBound(1) + 1; }
            set { this.in_Tensor = new double[this.in_Tensor.GetUpperBound(0), value]; }
        }

        /// <summary>
        /// This property returns the Tensor as an array.
        /// </summary>
        public double[,] AsArray
        {
            get { return this.in_Tensor; }
        }

        /// <summary>
        /// Gets the Tensor as a row major array.
        /// </summary>
        public double[] AsRowMajorArray
        {
            get
            {
                //  Storage for the array.
                List<double> ar = new List<double>();

                //  Create the array.
                for (int i = 0; i < Rows; i++)
                    for (int j = 0; j < Columns; j++)
                        ar.Add(this[i, j]);

                //  Return the array.
                return ar.ToArray();
            }
        }

