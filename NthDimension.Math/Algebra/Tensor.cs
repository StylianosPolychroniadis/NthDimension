        #region "Add Matrices"
        /// <summary>
        /// Returns the summation of two tensors with compatible 
        /// dimensions.
        /// In case of an error the error is raised as an exception.
        /// </summary>
        /// <param name="tensor1">First array in the summation</param>
        /// <param name="tensor2">Second array in the summation</param>
        /// <returns>Sum of Mat1 and Mat2 as an array</returns>
        public static double[,] Add(double[,] tensor1, double[,] tensor2)
        {
            double[,] sol;
            int i, j;
            int Rows1, Cols1;
            int Rows2, Cols2;

            //Find The dimensions !!
            try
            {
                Find_R_C(tensor1, out Rows1, out Cols1);
                Find_R_C(tensor2, out Rows2, out Cols2);
            }
            catch
            {
                throw new TensorNullException();
            }

            if (Rows1 != Rows2 || Cols1 != Cols2) throw new TensorDimensionException();

            sol = new double[Rows1 + 1, Cols1 + 1];

            for (i = 0; i <= Rows1; i++)
                for (j = 0; j <= Cols1; j++)
                {
                    sol[i, j] = tensor1[i, j] + tensor2[i, j];
                }

            return sol;
        }

        /// <summary>
        /// Returns the summation of two tensors with compatible 
        /// dimensions.
        /// In case of an error the error is raised as an exception.
        /// </summary>
        /// <param name="Tensor1">First Tensor in the summation</param>
        /// <param name="Tensor2">Second Tensor in the summation</param>
        /// <returns>Sum of Mat1 and Mat2 as a Tensor object</returns>
        public static Tensor Add(Tensor Tensor1, Tensor Tensor2)
        { return new Tensor(Add(Tensor1.in_Tensor, Tensor2.in_Tensor)); }

        /// <summary>
        /// Returns the summation of two matrices with compatible 
        /// dimensions.
        /// In case of an error the error is raised as an exception.
        /// </summary>
        /// <param name="Tensor1">First Tensor object in the summation</param>
        /// <param name="Tensor2">Second Tensor object in the summation</param>
        /// <returns>Sum of Mat1 and Mat2 as a Tensor object</returns>
        public static Tensor operator +(Tensor Tensor1, Tensor Tensor2)
        { return new Tensor(Add(Tensor1.in_Tensor, Tensor2.in_Tensor)); }
