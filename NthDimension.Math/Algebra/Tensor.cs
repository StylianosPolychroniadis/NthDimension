        /// <returns>Cross product of V1 and V2 as a Tensor (dimension [3,1])</returns>
        public static Tensor CrossProduct(Tensor V1, Tensor V2)
        { return (new Tensor((CrossProduct(V1.in_Tensor, V2.in_Tensor)))); }
