        #region "Subtract Tensors"

        /// <summary>
        /// Returns the difference of two tensors with compatible 
        /// dimensions.
        /// In case of an error the error is raised as an exception.
        /// </summary>
        /// <param name="tensor1">First array in the subtraction</param>
        /// <param name="tensor2">Second array in the subtraction</param>
        /// <returns>Difference of Mat1 and Mat2 as an array</returns>
        public static double[,] Subtract(double[,] tensor1, double[,] tensor2)
        {
            double[,] sol;
            int i, j;
            int Rows1, Cols1;
            int Rows2, Cols2;

            //Find The dimensions !!
            try
            {
                Find_R_C(tensor1, out Rows1, out Cols1);
                Find_R_C(tensor2, out Rows2, out Cols2);
            }
            catch
            {
                throw new TensorNullException();
            }

            if (Rows1 != Rows2 || Cols1 != Cols2) throw new TensorDimensionException();

            sol = new double[Rows1 + 1, Cols1 + 1];

            for (i = 0; i <= Rows1; i++)
                for (j = 0; j <= Cols1; j++)
                {
