using System;
using System.Collections.Generic;
using System.Linq;
using System.Xml.Serialization;

namespace NthDimension.Collections
{
    /// <summary>
    /// A thread-safe dictionary collection
    /// </summary>
    /// <typeparam name="TKey"></typeparam>
    /// <typeparam name="TValue"></typeparam>
    [XmlRoot("dictionary")]
    public class ThreadSafeDictionary<TKey, TValue> : IDictionary<TKey, TValue>, IXmlSerializable
    {
        private Dictionary<TKey, ValueWrapper> backing;
        private object backingLock = new object();

        #region IXmlSerializable Members
        public System.Xml.Schema.XmlSchema GetSchema()
        {
            return null;
        }

        public void ReadXml(System.Xml.XmlReader reader)
        {
            XmlSerializer keySerializer = new XmlSerializer(typeof(TKey));
            XmlSerializer valueSerializer = new XmlSerializer(typeof(TValue));

            reader.ReadStartElement("ndimension.live-database");

            while (reader.NodeType != System.Xml.XmlNodeType.EndElement)
            {
                reader.ReadStartElement("item");

                reader.ReadStartElement("key");
                TKey key = (TKey)keySerializer.Deserialize(reader);
                reader.ReadEndElement();

                reader.ReadStartElement("value");
                TValue value = (TValue)valueSerializer.Deserialize(reader);
                reader.ReadEndElement();

                this.Add(key, value);

                reader.ReadEndElement();
                reader.MoveToContent();
            }
            reader.ReadEndElement();
        }

        public void WriteXml(System.Xml.XmlWriter writer)
        {
            XmlSerializer keySerializer = new XmlSerializer(typeof(TKey));
            XmlSerializer valueSerializer = new XmlSerializer(typeof(TValue));

            writer.WriteStartElement("ndimension.live-database");
            foreach (TKey key in this.Keys)
            {
                writer.WriteStartElement("item");

                writer.WriteStartElement("key");
                keySerializer.Serialize(writer, key);
                writer.WriteEndElement();

                writer.WriteStartElement("value");
                TValue value = this[key];
                valueSerializer.Serialize(writer, value);
                writer.WriteEndElement();

                writer.WriteEndElement();
            }
            writer.WriteEndElement();
        }
        #endregion

        /// <summary>
        /// Exposes a synchronization object that can be used for locking operations while enumerating over the HexaDictionary
        /// </summary>
        public object SyncRoot
        {
            get { return backingLock; }
        }

        #region Constructors
        public ThreadSafeDictionary()
        {
            backing = new Dictionary<TKey, ValueWrapper>();
        }

        public ThreadSafeDictionary(int capacity)
        {
            backing = new Dictionary<TKey, ValueWrapper>(capacity);
        }

        public ThreadSafeDictionary(IEqualityComparer<TKey> comparer)
        {
            backing = new Dictionary<TKey, ValueWrapper>(comparer);
        }

        public ThreadSafeDictionary(int capacity, IEqualityComparer<TKey> comparer)
        {
            backing = new Dictionary<TKey, ValueWrapper>(capacity, comparer);
        }
        #endregion

        #region IDictionary<TKey,TValue> Members

        public void Add(TKey key, TValue value)
        {
            lock (backingLock)
            {
                try
                {
                    if (!ContainsKey(key))
                    {
                        backing.Add(key, new ValueWrapper(value));
                    }
                }
                catch (Exception e)
                {
                    string l = e.Message;
                }
            }
        }

        public bool ContainsKey(TKey key)
        {
            lock (backingLock)
            {
                return backing.ContainsKey(key);
            }
        }

        public ICollection<TKey> Keys
        {
            get
            {
                lock (backingLock)
                {
                    return backing.Keys;
                }
            }
        }

        public bool Remove(TKey key)
        {
            lock (backingLock)
            {
                return backing.Remove(key);
            }
        }

        public bool TryGetValue(TKey key, out TValue value)
        {
            lock (backingLock)
            {
                ValueWrapper wrapper;

                if (backing.TryGetValue(key, out wrapper))
                {
                    value = wrapper.Value;

                    return true;
                }
                else
                {
                    value = default(TValue);

                    return false;
                }
            }
        }

        public ICollection<TValue> Values
        {
            get
            {
                lock (backingLock)
                {
                    return backing.Values.Select(x => x.Value).ToArray();
                }
            }
        }

        public TValue this[TKey key]
        {
            get
            {
                lock (backingLock)
                {
                    return backing[key].Value;
                }
            }
            set
            {
                lock (backingLock)
                {
                    backing[key] = new ValueWrapper(value);
                }
            }
        }

        #endregion

        #region ICollection<KeyValuePair<TKey,TValue>> Members

        void ICollection<KeyValuePair<TKey, TValue>>.Add(KeyValuePair<TKey, TValue> item)
        {
            Add(item.Key, item.Value);
        }

        public void Clear()
        {
            lock (backingLock)
            {
                backing.Clear();
            }
        }

        public bool Contains(KeyValuePair<TKey, TValue> item)
        {
            lock (backingLock)
            {
                ValueWrapper wrapper;

                if (backing.TryGetValue(item.Key, out wrapper)) return object.ReferenceEquals(wrapper.Value, item.Value);

                return false;
            }
        }

        public void CopyTo(KeyValuePair<TKey, TValue>[] array, int arrayIndex)
        {
            lock (backingLock)
            {
                KeyValuePair<TKey, ValueWrapper>[] temp = new KeyValuePair<TKey, ThreadSafeDictionary<TKey, TValue>.ValueWrapper>[Count];

                ((ICollection<KeyValuePair<TKey, ValueWrapper>>)backing).CopyTo(temp, 0);

                for (int i = 0; i < temp.Length; i++) array[i + arrayIndex] = new KeyValuePair<TKey, TValue>(temp[i].Key, temp[i].Value.Value);
            }
        }

        public int Count
        {
            get
            {
                lock (backingLock)
                {
                    return backing.Count;
                }
            }
        }

        public bool IsReadOnly
        {
            get
            {
                lock (backingLock)
                {
                    return ((ICollection<KeyValuePair<TKey, ValueWrapper>>)backing).IsReadOnly;
                }
            }
        }

        public bool Remove(KeyValuePair<TKey, TValue> item)
        {
            lock (backingLock)
            {
                ValueWrapper wrapper;

                if (backing.TryGetValue(item.Key, out wrapper) && object.ReferenceEquals(wrapper.Value, item.Value))
                {
                    return backing.Remove(item.Key);
                }

                return false;
            }
        }

        #endregion

        #region IEnumerable<KeyValuePair<TKey,TValue>> Members

        public IEnumerator<KeyValuePair<TKey, TValue>> GetEnumerator()
        {
            lock (backingLock)
            {
                foreach (var pair in backing) yield return new KeyValuePair<TKey, TValue>(pair.Key, pair.Value.Value);
            }
        }

        #endregion

        #region IEnumerable Members

        System.Collections.IEnumerator System.Collections.IEnumerable.GetEnumerator()
        {
            return GetEnumerator();
        }

        #endregion

        /// <summary>
        /// Provides a lightweight wrapper that acts either as a value container or a placeholder for an uninitialized value.
        /// </summary>
        private class ValueWrapper
        {
            public TValue Value { get; private set; }
            private object valueLock;
            private bool isCreated;
            private Func<TValue> constructor;

            public void WaitForInitialization()
            {
                //System.Windows.Forms.Application.DoEvents(); 
                object sync = valueLock;

                if (sync != null)
                {
                    lock (sync)
                    {
                        if (!isCreated)
                        {
                            isCreated = true;

                            Value = constructor();

                            valueLock = null;
                        }
                    }
                }
            }

            public ValueWrapper(TValue value)
            {
                Value = value;
                isCreated = true;
            }

            public ValueWrapper(Func<TValue> constructor)
            {
                valueLock = new object();
                isCreated = false;
                this.constructor = constructor;
            }
        }

        /// <summary>
        /// Retrieves or creates an object with the specified key. If the requested object does not exist or is in the process of being created,
        /// the current thread will block until construction is complete. Requests for other objects during the creation process will execute normally.
        /// </summary>
        /// <param name="key">The identification key of the object</param>
        /// <param name="constructor">A delegate that can construct the requested value if it does not exist. This will only be called if the 
        /// requested key is not present in the dictionary.</param>
        /// <returns>The existing value, if present. If not present, it returns the return value of the supplied creation delegate.</returns>
        public TValue GetOrCreate(TKey key, Func<TValue> constructor)
        {
            ValueWrapper wrapper;

            lock (backingLock)
            {
                if (!backing.TryGetValue(key, out wrapper))
                {
                    wrapper = new ThreadSafeDictionary<TKey, TValue>.ValueWrapper(constructor);

                    backing.Add(key, wrapper);
                }
            }

            wrapper.WaitForInitialization();

            return wrapper.Value;
        }
    }
}
