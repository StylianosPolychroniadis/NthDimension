using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace NthDimension.Procedural.Text
{
    /// <summary>
    /// Casing styles
    /// </summary>
    public enum Casing
    {
        /// <summary>
        /// example: annoyedWombat
        /// </summary>
        CamelCase,

        /// <summary>
        /// example: AnnoyedWombat
        /// </summary>
        PascalCase,

        /// <summary>
        /// example: ANNOYEDWOMBAT
        /// </summary>
        UpperCase,

        /// <summary>
        /// example: annoyedwombat
        /// </summary>
        LowerCase
    }
}
